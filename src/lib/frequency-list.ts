export function getFrequencyBand(word: string): FrequencyBand {
  const lower = word.toLowerCase();
  if (BAND_2K.has(lower)) return "top2k";
  if (BAND_5K.has(lower)) return "top5k";
  if (BAND_10K.has(lower)) return "top10k";
  return "rare";
}

